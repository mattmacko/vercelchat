import { type InferSelectModel, sql } from "drizzle-orm";
import {
  boolean,
  foreignKey,
  index,
  integer,
  json,
  jsonb,
  pgTable,
  primaryKey,
  text,
  timestamp,
  uniqueIndex,
  uuid,
  varchar,
} from "drizzle-orm/pg-core";
import type { AppUsage } from "../usage";

export const user = pgTable(
  "User",
  {
    id: uuid("id").primaryKey().notNull().defaultRandom(),
    email: varchar("email", { length: 64 }).notNull(),
    password: varchar("password", { length: 64 }),
    tier: varchar("tier", { length: 16 }).notNull().default("free"),
    lifetimeAccess: boolean("lifetimeAccess").notNull().default(false),
    authProvider: varchar("authProvider", { length: 16 })
      .notNull()
      .default("credentials"),
    googleId: text("googleId"),
    emailVerifiedAt: timestamp("emailVerifiedAt"),
    stripeCustomerId: text("stripeCustomerId"),
    stripeSubscriptionId: text("stripeSubscriptionId"),
    proExpiresAt: timestamp("proExpiresAt"),
    messagesSentCount: integer("messagesSentCount").notNull().default(0),
  },
  (table) => ({
    emailUnique: uniqueIndex("User_email_unique").on(table.email),
    googleIdUnique: uniqueIndex("User_googleId_unique")
      .on(table.googleId)
      .where(sql`${table.googleId} is not null`),
    tierIdx: index("User_tier_idx").on(table.tier),
    stripeCustomerUnique: uniqueIndex("User_stripeCustomerId_unique")
      .on(table.stripeCustomerId)
      .where(sql`${table.stripeCustomerId} is not null`),
    stripeSubscriptionUnique: uniqueIndex("User_stripeSubscriptionId_unique")
      .on(table.stripeSubscriptionId)
      .where(sql`${table.stripeSubscriptionId} is not null`),
  })
);

export type User = InferSelectModel<typeof user>;

export const stripeEventLog = pgTable("stripe_event_log", {
  id: text("id").primaryKey(),
  status: varchar("status", { length: 16 }).notNull().default("processing"),
  seenAt: timestamp("seen_at", { withTimezone: true }).defaultNow(),
  processingStartedAt: timestamp("processing_started_at", {
    withTimezone: true,
  }),
  processedAt: timestamp("processed_at", { withTimezone: true }),
  attemptCount: integer("attempt_count").notNull().default(0),
  lastError: text("last_error"),
});

export const chat = pgTable("Chat", {
  id: uuid("id").primaryKey().notNull().defaultRandom(),
  createdAt: timestamp("createdAt").notNull(),
  title: text("title").notNull(),
  userId: uuid("userId")
    .notNull()
    .references(() => user.id),
  visibility: varchar("visibility", { enum: ["public", "private"] })
    .notNull()
    .default("private"),
  lastContext: jsonb("lastContext").$type<AppUsage | null>(),
});

export type Chat = InferSelectModel<typeof chat>;

